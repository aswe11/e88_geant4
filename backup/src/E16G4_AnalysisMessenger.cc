#include "E16G4_AnalysisMessenger.hh"
#include "E16G4_Analysis.hh"
#include "G4UIdirectory.hh"
#include "G4UIcmdWithAString.hh"
#include "G4UIcmdWithAnInteger.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

E16G4_AnalysisMessenger::E16G4_AnalysisMessenger(
                                          E16G4_Analysis* ana)
:Ana(ana)
{
  anaDir = new G4UIdirectory("/E16G4/ana/");
  anaDir->SetGuidance("Analysis control");
   
  OutFileCmd = new G4UIcmdWithAString("/E16G4/ana/setOutFile",this);
  OutFileCmd->SetGuidance("Set Output File for the incident particle.");
  OutFileCmd->SetParameterName("OutFile",true);
  OutFileCmd->AvailableForStates(G4State_PreInit,G4State_Idle);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

 E16G4_AnalysisMessenger::~E16G4_AnalysisMessenger()
{
  delete OutFileCmd;
  delete anaDir;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void E16G4_AnalysisMessenger::SetNewValue(
                                        G4UIcommand* command, G4String newValue)
{ 
  if( command == OutFileCmd )
   { Ana->SetOutFile(newValue);}
}

